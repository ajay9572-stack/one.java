public class Five {
         // find the positive or negative number
    public static void main(String[] args) {
        int no = 5;
        if (no > 0) {
            System.out.println("number is positive");
        } else if (no < 0) {
            System.out.println("number is negative");
        } else {
            System.out.println("number is zero");
        }
    }
}
